#include <iostream>
using namespace std;

int main() {
	int first[21] = { 5000000, 3000000, 3000000, 2000000, 2000000, 2000000, 500000, 500000, 500000, 500000,
	300000, 300000, 300000, 300000, 300000, 100000, 100000, 100000, 100000, 100000, 100000 };
	int second[31] = { 5120000, 2560000, 2560000, 1280000, 1280000, 1280000, 1280000,
	640000, 640000, 640000, 640000, 640000, 640000, 640000, 640000, 320000, 320000, 320000,
	320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000, 320000 };
	int T; cin >> T;

	for (int i = 0; i < T; i++) {
		int a, b; cin >> a >> b;

		int priceA, priceB;
		if (a == 0 || a > 21) priceA = 0;
		else priceA = first[a - 1];
		if (b == 0 || b > 31) priceB = 0;
		else priceB = second[b - 1];

		cout << priceA + priceB << '\n';
	}
}
